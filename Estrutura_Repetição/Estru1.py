#✅ 1. Estruturas de Repetição
#🔎 O que é?
#Estruturas de repetição permitem que blocos de código sejam executados várias vezes. Usamos isso para automatizar tarefas e evitar repetições manuais.

#Contador com for

# Executa de 1 até 5, exibindo cada número
for i in range(1, 6):
    print(f"Contando: {i}")
print("Fim da contagem.")
