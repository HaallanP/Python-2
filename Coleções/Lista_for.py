#Coleções (Listas, Tuplas e Dicionários)
#🔎 O que é?
#Coleções armazenam vários valores em uma única variável. São úteis para trabalhar com grandes quantidades de dados de forma organizada.

# Lista For

# Lista de frutas e iteração sobre ela
frutas = ["maçã", "banana", "laranja"]
for fruta in frutas:
    print(f"Eu gosto de {fruta}")
